from io import BytesIO
from reportlab.platypus import (
    SimpleDocTemplate,
    Paragraph,
    Spacer,
    Preformatted
)
from reportlab.lib.styles import getSampleStyleSheet
from xml.sax.saxutils import escape


# ==========================================
# MAIN PDF GENERATOR
# ==========================================

def generate_pdf(
    title,
    content,
    source="Uploaded Document"
):

    try:

        buffer = BytesIO()

        doc = SimpleDocTemplate(
            buffer
        )

        styles = getSampleStyleSheet()

        story = []

        title_style = styles["Title"]
        heading_style = styles["Heading2"]
        body_style = styles["BodyText"]

        # -----------------------------
        # Title
        # -----------------------------

        story.append(
            Paragraph(
                escape(str(title)),
                title_style
            )
        )

        story.append(
            Spacer(1, 15)
        )

        # -----------------------------
        # Empty Content Check
        # -----------------------------

        if (
            content is None
            or
            str(content).strip() == ""
        ):

            content = (
                "No content generated."
            )

        # -----------------------------
        # Main Content
        # -----------------------------

        content = escape(
            str(content)
        )

        story.append(
            Preformatted(
                content,
                body_style
            )
        )

        story.append(
            Spacer(1, 20)
        )

        # -----------------------------
        # Source Information
        # -----------------------------

        story.append(
            Paragraph(
                "Source Information",
                heading_style
            )
        )

        story.append(
            Paragraph(
                escape(
                    str(source)
                ),
                body_style
            )
        )

        story.append(
            Spacer(1, 20)
        )

        # -----------------------------
        # Footer
        # -----------------------------

        story.append(
            Paragraph(
                "Generated by AI Teacher Assistant",
                body_style
            )
        )

        doc.build(
            story
        )

        pdf_bytes = (
            buffer.getvalue()
        )

        buffer.close()

        return pdf_bytes

    except Exception as e:

        return str(
            f"PDF Generation Error: {e}"
        ).encode()


# ==========================================
# NOTES PDF
# ==========================================

def notes_pdf(
    notes,
    source
):

    return generate_pdf(
        "Study Notes",
        notes,
        source
    )


